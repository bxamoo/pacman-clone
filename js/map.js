// ===== ステージデータ（横長4:3 / 非対称 / クリア可能） =====
// 0: 通路, 1: 壁, 2: ドット

const TILE = 24;
const COLS = 28;
const ROWS = 15;

/*
  特徴：
  - 左右非対称
  - 全てのドットに到達可能
  - トンネルなし
  - 袋小路は1箇所だけ
  - ゴーストハウスあり
  - パックマン初期位置とゴースト初期位置に合わせた構造
*/

const baseMap = [
  "1111111111111111111111111111",
  "1222222222222222222222222221",
  "1211112111112111111112111121",
  "1222222112222222222112222221",
  "1111112112111111112112111111",
  "1222212112111111112112222221",
  "1111112112220000002112111111",
  "1222222221111111111222222221",
  "1211112111111111111112111121",
  "1222222112222222222112222221",
  "1111112112111111112112111111",
  "1222212112111111112112222221",
  "1222222222222222222222222221",
  "1211111111111111111111111121",
  "1111111111111111111111111111"
].map(r => r.split("").map(Number));

let map = baseMap.map(r => r.slice());

// 通路(0)をドット(2)に変換（ゴーストハウス部分は 0 のまま）
for (let y = 0; y < ROWS; y++) {
  for (let x = 0; x < COLS; x++) {
    if (map[y][x] === 0) map[y][x] = 2;
  }
}
